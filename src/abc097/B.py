x = int(input())
l = [1]

for maxb, p in zip([31, 10, 5, 3, 3, 2, 2, 2], range(2, 10)):
    for b in range(2, maxb+1):
        n = pow(b, p)
        if n <= x:
            l.append(n)

print(max(l))


